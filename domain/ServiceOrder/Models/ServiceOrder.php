    protected $casts = [
        'customer_form' => 'json',
        'service_price' => 'float',
        'additional_charges' => 'json',
        'billing_cycle' => BillingCycleEnum::class,
        'pay_upfront' => 'boolean',
        'is_subscription' => 'boolean',
        'needs_approval' => 'boolean',
        'is_auto_generated_bill' => 'boolean',
